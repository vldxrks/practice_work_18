# Початковий текстовий масив
words = ["папір", "вода", "башта", "канал", "висота", "об’єм"]

print("Початковий масив:")
print(words)

# Злиття 2-го і 4-го елементів
merged = words[1] + words[3]

# Записуємо отримане слово на 2-ге місце
words[1] = merged

# Видаляємо 4-й елемент (індекс 3 після зміни)
del words[3]

print("\nОновлений масив:")
print(words)
