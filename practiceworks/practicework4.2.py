# Введення масиву слів з клавіатури
words = input("Введіть слова через кому: ").split(",")

# Прибираємо зайві пробіли
words = [w.strip() for w in words]

print("\nПочатковий масив:")
print(words)

# Перевірка, що масив має достатню кількість елементів
if len(words) >= 4:
    # Злиття 2-го і 4-го елементів
    merged = words[1] + words[3]
    
    # Записуємо отримане слово на 2-ге місце
    words[1] = merged
    
    # Видаляємо 4-й елемент
    del words[3]
    
    print("\nОновлений масив:")
    print(words)
else:
    print("\nПомилка: масив має містити щонайменше 4 елементи!")
