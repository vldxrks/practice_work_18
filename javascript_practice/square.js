/**
 * Обчислює квадрат заданого числа.
 * @param {number} num - Число.
 * @returns {number} Квадрат числа.
 */
function square(num) {
  return num * num;
}

// Приклад використання:
let result1 = square(5);
console.log(result1); // Виведе: 25

let result2 = square(10);
console.log(result2); // Виведе: 100