/**
 * Стрілкова функція, що обчислює периметр прямокутника.
 * @param {number} a - Довжина першої сторони.
 * @param {number} b - Довжина другої сторони.
 * @returns {number} Периметр прямокутника.
 */
const perimeter = (a, b) => 2 * (a + b);

// Приклад використання:
console.log(perimeter(4, 6)); // Виведе: 20
console.log(perimeter(10, 3)); // Виведе: 26