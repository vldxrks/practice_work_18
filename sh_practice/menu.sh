#!/bin/bash
# menu.sh: Просте текстове меню

CHOICE=0 # Ініціалізація змінної вибору

# Цикл while повторюється, доки CHOICE не дорівнює 3
while [ "$CHOICE" -ne 3 ]; do
  echo "--- Головне Меню ---"
  echo "1 — Показати дату"
  echo "2 — Показати список файлів"
  echo "3 — Вийти"
  echo "----------------------"

  # Зчитування вибору користувача
  read -p "Введіть свій вибір (1-3): " CHOICE

  case $CHOICE in
    1)
      echo "Поточна дата та час:"
      date
      ;;
    2)
      echo "Список файлів у поточному каталозі:"
      ls -l
      ;;
    3)
      echo "Вихід із програми. До побачення!"
      ;;
    *)
      echo "Некоректний вибір. Спробуйте ще раз."
      ;;
  esac

  echo # Додатковий рядок для кращої читабельності
donexl