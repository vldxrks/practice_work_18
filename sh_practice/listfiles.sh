#!/bin/bash
# listfiles.sh: Перелік файлів .txt

echo "Пошук файлів .txt у поточному каталозі..."

# Цикл перебирає всі об'єкти, що відповідають масці *.txt
for file in *.txt; do
  # Перевіряємо, чи був знайдений хоча б один файл (запобігання виведення *.txt, якщо файлів немає)
  if [ -f "$file" ]; then
    echo "Знайдено файл: $file"
  fi
done