import math

# Введення катетів
a = float(input("Введіть перший катет: "))
b = float(input("Введіть другий катет: "))

# Перевірка існування трикутника
if a > 0 and b > 0:
    c = math.sqrt(a**2 + b**2)
    P = a + b + c
    print(f"Гіпотенуза = {c:.2f}")
    print(f"Периметр трикутника = {P:.2f}")
else:
    print("Трикутник не існує (катети повинні бути додатні).")
