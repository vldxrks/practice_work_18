import math

# Введення радіусів
R = float(input("Введіть зовнішній радіус R: "))
r = float(input("Введіть внутрішній радіус r: "))

# Перевірка правильності введення
if R > r:
    S = math.pi * (R**2 - r**2)
    print(f"Площа кільця = {S:.2f}")
else:
    print("Помилка: зовнішній радіус повинен бути більший за внутрішній!")
